/* ----------------------------------------------------------------------- */
typedef
   struct tagDBFHeader{ // 32 bytes
      unsigned char      Memo;    // Memo yes = 0x83 no = 0x03
      unsigned char      Y,M,D;   // Year month day
      unsigned long      NRecs;   // Записей в файле
      unsigned short int SHeader; // Размер заголовка с описаниями полей (32 + 32 * NFields + 1)
      unsigned short int SRecord; // Размер записи (SUM(fSize's) + 1)
      unsigned char      _1[20];  // unused
   }DBFHeader, *LPDBFHeader;
/* ----------------------------------------------------------------------- */
typedef
   struct tagDBFField{ // 32 bytes
      unsigned char fName[10];// Имя поля
      unsigned char _1;       // unused
      unsigned char fType;    /* Типы полей:
                                  0x43 - Символьное
                                  0x4E - Числовое
                                  0x4D - MEMO
                                  0x4C - Логическое
                                  0x44 - Дата */
      unsigned short int fOffset; // Смещение поля от начала записи
      unsigned short int _2;      // unused
      unsigned char  fSize;       // Размер поля
      unsigned char  fFloat;      // Количество знаков поле запятой(только для Числовых)
      unsigned char  _3[14];      // unused
   }DBFField, *LPDBFField;
/* ----------------------------------------------------------------------- */
typedef
   struct tagDBF {
      DBFHeader header;
      LPDBFField fields;        // Указатель на массив описаний полей данной базы
      unsigned int NumFields;   // Количество полей
      FILE *fdbf;               // Указатель на файл
      unsigned long Current;    // Номер тек. записи
      unsigned long oStartData; // Смещение до начала данных в файле
      unsigned char *Record;    // Массив байт для хранения записи, т.н. БУФЕР
   }DBF, *LPDBF;
/* ----------------------------------------------------------------------- */
typedef
   union tagREC{ // Объединение заголовка и описания поля: 32-байта
      DBFHeader h;
      DBFField  f;
   }DBFDefinition;
/* ----------------------------------------------------------------------- */
// Коды ошбок при работе с базой данных
typedef
   enum tagDBFError {
      DBF_OK,                      // Нет ошибок
      DBF_ERROR_NO_DATABASE,       // Нет открытой базы данных
      DBF_ERROR_EMPTY_DATABASE,    // База пуста
      DBF_ERROR_FILE_OPEN,         // Файл базы не открывается или не существует
      DBF_ERROR_FILE_READ,         // Ошибка при чтении из базы
      DBF_ERROR_FILE_WRITE,        // Ошибка записи базы
      DBF_ERROR_FILE_SEEK,         // Ошибка перемещения указателя по базе
      DBF_ERROR_FILE_END,          // НЕ ОШИБКА! Указатель в конце базы
      DBF_ERROR_MEMORY_ALLOCATE,   // Ошибка распределения памяти
      DBF_ERROR_DATABASE_STRUCTURE,// Ошибка во внутренней структуре базы
      DBF_ERROR_UNKNOWN_FIELD,     // Поле не описано в данной базе
      DBF_ERROR_LONG_VALUE,        // Слишком длинное значение для записи в поле
      DBF_ERROR_RECORD_NOT_FOUND,  // НЕ ОШИБКА! Не найдена запись по ключу
      DBF_ERROR_WRONG_TIME_FORMAT, // Неправильный формат времени
      DBF_ERROR_WRONG_DATE_FORMAT  // Неправильный формат даты
   }DBFError;
